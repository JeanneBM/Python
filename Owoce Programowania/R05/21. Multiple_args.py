# Ten program pokazuje przykład
# funkcji akceptującej dwa argumenty.

def main():
    print('Suma liczb 12 i 45 wynosi')
    show_sum(12, 45)

# Funkcja show_sum() akceptuje dwa
# argumenty i wyświetla ich sumę.
def show_sum(num1, num2):
    result = num1 + num2
    print(result)

# Wywołanie funkcji main().
main()


