# Ten program pokazuje przykład
# przekazania argumentu funkcji.

def main():
    value = 5
    show_double(value)

# Funkcja show_double() akceptuje argument
# i wyświetla jego podwojoną wartość.
def show_double(number):
    result = number * 2
    print(result)

# Wywołanie funkcji main().
main()


